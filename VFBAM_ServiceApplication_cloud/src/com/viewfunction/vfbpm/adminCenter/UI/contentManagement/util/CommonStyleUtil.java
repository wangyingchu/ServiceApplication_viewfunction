package com.viewfunction.vfbpm.adminCenter.UI.contentManagement.util;

public class CommonStyleUtil {
	 public static String formatCurrentItemStyle(String currentItemString) {
	        return "<b style='color:#ce0000;'>" + currentItemString + "</b>";
	 }

	 public static String formatParentItemStyle(String parentItemString) {
	        return "<b style='color:#000000;'>" + parentItemString + "</b>";
	 }	 
}